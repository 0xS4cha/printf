#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		sign;
	long	value;
	size_t	i;

	i = 0;
	value = 0;
	sign = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (ft_isdigit(nptr[i]))
	{
		value = value * 10 + (nptr[i] - '0');
		i++;
	}
	return ((int)(value * sign));
}
